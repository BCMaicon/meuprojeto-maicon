    private void btconfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btconfirmarActionPerformed
        //Verificar se acertou
         perguntas.remove(0);
        confirmar++;
        PerguntaDAO dao = new PerguntaDAO();
        Audio audio = new Audio();
        ft = new FimTela();
        jc = new JogoCompleto();
        String z = "";
        Boolean acertou = false;

        if (opcao1.isSelected() == true) {
            z = "A";
            //atual = premio;
        } else if (opcao2.isSelected() == true) {
            z = "B";
            //  atual = premio;
        } else if (opcao3.isSelected() == true) {
            z = "C";
            //   atual = premio;
        } else if (opcao4.isSelected() == true) {
            z = "D";
            //   atual = premio;

        }

        atual = atual + premio;

        if (!perguntaatual.getCerta().equals(z)) {
            JOptionPane.showMessageDialog(rootPane, "Resposta errada");

            jc.setJog(jog);
            jc.setGanhos(errar);
            ft.jc = jc;
            ft.setVisible(true);
            this.setVisible(false);
        } else {
            //JOptionPane.showMessageDialog(rootPane, "Resposta certa");
            audio.tocar("certa.wav");
            acertou = true;

            if (acertou == true) {

                jc.setGanhos(acertar);

                
                if (nivel == 4 && confirmar == 5) {

                    nivel = 5;
                    perguntas = dao.listarNivel(nivel);
                    
                    
                } else if (confirmar == 5) {
                    nivel++;
                    perguntas = dao.listarNivel(nivel);
                    confirmar = 0;
                }

                if (nivel == 2) {
                    premio = 20000;
                } else if (nivel == 3) {
                    premio = 30000;
                } else if (nivel == 4) {
                    premio = 60000;
                } else if (nivel == 5 && confirmar == 6) {
                    premio = 1000000;
                    //nivel++;                   
                    if (acertou == false) {
                        errar = 0;
                        jc.setGanhos(errar);
                    } else {
                        jc.setGanhos(acertar);
                    }
                    jc.setJog(jog);
                    ft.jc = jc;
                    ft.setVisible(true);
                    this.setVisible(false);
                    return;
                }

                opcao1.setEnabled(true);
                opcao2.setEnabled(true);
                opcao3.setEnabled(true);
                opcao4.setEnabled(true);

                perguntaatual = perguntas.get(0);
               
                lblpergunta.setText(perguntaatual.getEnunciado());
                opcao1.setText(perguntaatual.getA());
                opcao2.setText(perguntaatual.getB());
                opcao3.setText(perguntaatual.getC());
                opcao4.setText(perguntaatual.getD());

                bg.clearSelection();

            }
            if(nivel==5&&confirmar==5){
                    btcartas.setEnabled(false);
                    btpular.setEnabled(false);
                    btparar.setEnabled(false);
                    atual = 0;
                    errar = 0;
                    acertar = 1000000;
                    lblparar.setText(atual.toString());
                    lblerrar.setText(errar.toString());
                    lblacertar.setText(acertar.toString());
            } else {
            errar = atual / 2;
            acertar = premio + atual;
            lblparar.setText(atual.toString());
            lblerrar.setText(errar.toString());
            lblacertar.setText(acertar.toString());
            }
        }

    }//GEN-LAST:event_btconfirmarActionPerformed

