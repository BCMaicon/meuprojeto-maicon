    public void Cartas(Integer c) {
        if (c == 3) {
            if (perguntaatual.getCerta().equals("A")) {
                opcao2.setEnabled(false);
                opcao3.setEnabled(false);
                opcao4.setEnabled(false);
            } else if (perguntaatual.getCerta().equals("B")) {
                opcao1.setEnabled(false);
                opcao3.setEnabled(false);
                opcao4.setEnabled(false);
            } else if (perguntaatual.getCerta().equals("C")) {
                opcao1.setEnabled(false);
                opcao2.setEnabled(false);
                opcao4.setEnabled(false);
            } else if (perguntaatual.getCerta().equals("D")) {
                opcao1.setEnabled(false);
                opcao2.setEnabled(false);
                opcao3.setEnabled(false);
            }

        } else if (c == 1) {
            if (perguntaatual.getCerta().equals("A")) {

                opcao2.setEnabled(false);

            } else if (perguntaatual.getCerta().equals("B")) {
                opcao1.setEnabled(false);

            } else if (perguntaatual.getCerta().equals("C")) {

                opcao4.setEnabled(false);
            } else if (perguntaatual.getCerta().equals("D")) {

                opcao3.setEnabled(false);

            }

        } else if (c == 2) {
            if (perguntaatual.getCerta().equals("A")) {

                opcao2.setEnabled(false);
                opcao3.setEnabled(false);

            } else if (perguntaatual.getCerta().equals("B")) {
                opcao1.setEnabled(false);
                opcao4.setEnabled(false);
            } else if (perguntaatual.getCerta().equals("C")) {

                opcao2.setEnabled(false);
                opcao4.setEnabled(false);
            } else if (perguntaatual.getCerta().equals("D")) {
                opcao1.setEnabled(false);
                opcao2.setEnabled(false);

            }

        } else {
            opcao1.setEnabled(true);
            opcao2.setEnabled(true);
            opcao4.setEnabled(true);
            opcao3.setEnabled(true);

        }
        cartas = 0;
    }

