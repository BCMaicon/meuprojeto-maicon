    private void consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarActionPerformed
        pergunta p = new pergunta();
        String perg = JOptionPane.showInputDialog("Informe o nome a ser pesquisado:");
        int posicaoachou = 0;
        boolean enc = false;
        for (pergunta pergunta : lista) {
            if (perg.equalsIgnoreCase(pergunta.getEnunciado())) {
                posicao = posicaoachou;
                enc = true;
                
                txtenunciado.setText(pergunta.getEnunciado());
                a.setText(pergunta.getA());
                b.setText(pergunta.getC());
                c.setText(pergunta.getB());
                d.setText(pergunta.getD());
                txtid.setText(pergunta.getId().toString());
                boxnivel.setSelectedItem(pergunta.getNivel().toString());
                boxcerta.setSelectedItem(pergunta.getCerta());
                break;
            }
            posicaoachou++;
        }
        if (enc == false) {
            JOptionPane.showMessageDialog(null, "Essa pergunta não está cadastrada");
        }
    }//GEN-LAST:event_consultarActionPerformed

    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed
        if (txtid.getText().isEmpty() == false)
        {
            PerguntaDAO dao = new PerguntaDAO();
            Boolean deucerto = dao.excluir(lista.get(posicao));
            
            if (deucerto == true)
            {
                JOptionPane.showMessageDialog(rootPane, "Excluído com sucesso");
                lista = dao.listar();
                Limpar();
            }else
            {
                 JOptionPane.showMessageDialog(rootPane, "Erro ao excluir");
            }
        }
        
        
        lista.remove(lista.get(posicao));
        Limpar();
    }//GEN-LAST:event_excluirActionPerformed

