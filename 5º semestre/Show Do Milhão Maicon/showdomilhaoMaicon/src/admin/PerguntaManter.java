    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        pergunta p = new pergunta();

        Boolean y = false;

        if (txtenunciado.getText().isEmpty() || b.getText().isEmpty() || c.getText().isEmpty() || d.getText().isEmpty() || a.getText().isEmpty() || boxnivel.getSelectedIndex() == 0 || boxcerta.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos!!!");
        } else {

            y = true;
            p.setEnunciado(txtenunciado.getText());
            p.setA(a.getText());
            p.setB(b.getText());
            p.setC(c.getText());
            p.setD(d.getText());
            p.setCerta(boxcerta.getSelectedItem().toString());
            p.setNivel(Integer.parseInt(boxnivel.getSelectedItem().toString()));

            if (y == true) {

                PerguntaDAO dao = new PerguntaDAO();
                dao.inserir(p);

                JOptionPane.showMessageDialog(rootPane, "Pergunta cadastrada com sucesso!!!");
                
                lista = dao.listar();
                Limpar();
            }

        }
    }//GEN-LAST:event_cadastrarActionPerformed

