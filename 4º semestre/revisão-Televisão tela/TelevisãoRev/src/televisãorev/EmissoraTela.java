    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        EmissoraRev e = new EmissoraRev();
        String nome, endereco;
        boolean exibicao;

        e.setNome(jTextField1.getText());
        e.setEndereco(jTextField2.getText());

        if (jComboBox1.getSelectedItem().toString().equals("Aberta") == true) {
            e.setExibicao(true);
        } else {
            e.setExibicao(false);
        }
        lista.add(e);
        Limpar();
        JOptionPane.showMessageDialog(null, " Emissora cadastrada com sucesso!!!");

        if (jTextField1.equals("") || jTextField2.equals("") || jComboBox1.equals(0)) {
            JOptionPane.showMessageDialog(null, "Preencha todas as lacunas!");
        } else {
            JOptionPane.showMessageDialog(null, "Nome:" + jTextField1 + "Endereço:" + jTextField2 + "Exibição:" + jComboBox1);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

