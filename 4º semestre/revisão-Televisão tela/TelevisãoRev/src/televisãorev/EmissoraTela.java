    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        posicao = 0;
        //pegar o primeiro elemento da lista:
        EmissoraRev elemento = lista.get(0);
        if (lista.size() > 0) {

            jTextField1.setText(elemento.getNome());
            jTextField2.setText(elemento.getEndereco());
            if (elemento.isExibicao() == true) {
                jComboBox1.setSelectedIndex(2);
            } else {
                jComboBox1.setSelectedIndex(1);
            }

        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        posicao = lista.size() - 1;
        if (posicao > 0) {
            if (lista.size() > 0) {
                EmissoraRev elemento = lista.get(posicao - 1);

                jTextField1.setText(elemento.getNome());
                jTextField2.setText(elemento.getEndereco());
                if (elemento.isExibicao() == true) {
                    jComboBox1.setSelectedIndex(2);
                } else {
                    jComboBox1.setSelectedIndex(1);
                }
            }
        } else {
            EmissoraRev elemento = lista.get(0);
            if (lista.size() > 0) {
                jTextField1.setText(elemento.getNome());
                jTextField2.setText(elemento.getEndereco());
                if (elemento.isExibicao() == true) {
                    jComboBox1.setSelectedIndex(2);
                } else {
                    jComboBox1.setSelectedIndex(1);
                }

            }

        }
    }//GEN-LAST:event_jButton4ActionPerformed

